     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $usuario = User::findOrFail($id);
        $request->validate([
            'password_nueva' => 'required|min:8',
            'verificacion_password_nueva' => 'required|min:8',
        ]);
        if ($request->password_nueva == $request->verificacion_password_nueva) {
            $usuario->password = Hash::make($request->password_nueva);
            $usuario->save();
            return view('modulos.editarinfo.resumen', compact('usuario'));
        } else {
            return redirect()->route('editarpasswd.index')->with('error', 'Las contrase√±as no coinciden');
        }
